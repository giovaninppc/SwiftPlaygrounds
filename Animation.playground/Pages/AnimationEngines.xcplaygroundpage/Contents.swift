 ## Animation Engines

 Mas nossa Giovani, muito programático isso aí.
 Sim, é mesmo.

 Mas a graça é: Essa é a base de todo e qualquer sistema de animações do MUNDO MUAHAHAHAHAHA (muito provavelmente, não parei pra checar mas creio fortemente que sim, pq a grande maioria é mesmo).

 Aqui vamos diferenciar uma animação _computadorizada_ de um GIF, ou das animações da Disney feitas à mão. Esses caras, são uma sequência de imagens, que quando exibidas nos dão a sensação de animação.

 Mas se olharmos um software feito pra animações, como o After Effects da Adobe, o princípio se mantém.

 - Você define o estado inicial dos seus objetos

 - Move eles pro seu estado final no tempo

 - Ajusta as propriedades (cor, tamanho, opacidade...)

 E todo o meio do caminho vai ser interpolado pela sua engine!

 ![](example.gif)

 > Animações desse tipo não envolvem apenas 1 interpolação 2D, mas sim várias e várias interpolações de vários objetos (2 ou 3D) ao longo go tempo.

 E a graça está em não apenas animar uma coisa de cada vez, mas sim várias propriedades da sua view ao mesmo tempo.

 E podemos definir qual a função que vai interpolar os pontos para nos dar resultados visuais diferentes.

